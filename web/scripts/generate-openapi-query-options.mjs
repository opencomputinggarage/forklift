import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import YAML from "yaml";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const webRoot = path.resolve(__dirname, "..");
const specPath = path.resolve(webRoot, "../internal/openapi/openapi.yaml");
const outPath = path.resolve(webRoot, "src/generated/openapi-query-options.ts");

const methodOrder = ["get", "post", "put", "patch", "delete"];
const apiBasePath = "/api/v1";

const responseTypeOverrides = new Map([
  ["GET /api/v1/me", "Me"],
  ["GET /api/v1/version", "Version"],
  ["GET /api/v1/repositories", "Repository[]"],
  ["GET /api/v1/repositories/{id}", "Repository"],
  ["GET /api/v1/repositories/{id}/audit-logs", "AuditLogList"],
  ["GET /api/v1/tokens", "Token[]"],
  ["GET /api/v1/users", "User[]"],
  ["GET /api/v1/users/{id}/tokens", "Token[]"],
  ["GET /api/v1/roles", "Role[]"],
  ["GET /api/v1/approvals", "ApprovalList"],
  ["GET /api/v1/approvals/count", "{ count: number }"],
  // The OpenAPI schema is PackageApproval; the web client type is Approval.
  ["GET /api/v1/approvals/{id}", "Approval"],
  ["GET /api/v1/version-denies", "VersionDenyList"],
]);

const typeImports = new Set();

function toPascalCase(value) {
  return value
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join("");
}

function singularize(value) {
  if (value.endsWith("ies")) return `${value.slice(0, -3)}y`;
  if (value.endsWith("s") && !value.endsWith("ss")) return value.slice(0, -1);
  return value;
}

function operationName(method, routePath, usedNames) {
  const segments = routePath
    .replace(apiBasePath, "")
    .split("/")
    .filter(Boolean);
  const lastSegment = segments.at(-1) ?? "root";
  const hasPathParam = segments.some((segment) => segment.startsWith("{"));

  let prefix =
    method === "get" && !lastSegment.startsWith("{") && lastSegment.endsWith("s")
      ? "list"
      : method;
  const nameParts = segments
    .filter((segment) => !segment.startsWith("{"))
    .map((segment, index) => {
      const shouldSingularize =
        hasPathParam && index === 0 && segment.endsWith("s");
      return toPascalCase(shouldSingularize ? singularize(segment) : segment);
    });

  let name = `${prefix}${nameParts.join("") || "Root"}`;
  let suffix = 2;
  while (usedNames.has(name)) {
    name = `${prefix}${nameParts.join("") || "Root"}${suffix}`;
    suffix += 1;
  }
  usedNames.add(name);
  return name;
}

function collectParameters(pathItem, operation) {
  return [...(pathItem.parameters ?? []), ...(operation.parameters ?? [])];
}

function schemaType(schema) {
  if (!schema) return "string";
  const schemaValue = Array.isArray(schema.type) ? schema.type[0] : schema.type;
  switch (schemaValue) {
    case "integer":
    case "number":
      return "number";
    case "boolean":
      return "boolean";
    default:
      return "string";
  }
}

function paramsType(parameters, location) {
  const params = parameters.filter((parameter) => parameter.in === location);
  if (params.length === 0) return null;
  const fields = params.map((parameter) => {
    const optional = parameter.required ? "" : "?";
    return `${parameter.name}${optional}: ${schemaType(parameter.schema)}`;
  });
  return `{ ${fields.join("; ")} }`;
}

function responseType(method, routePath, operation) {
  const key = `${method.toUpperCase()} ${routePath}`;
  const override = responseTypeOverrides.get(key);
  if (override) {
    for (const importName of override.matchAll(/\b[A-Z][A-Za-z0-9]*\b/g)) {
      if (importName[0] !== "Array") typeImports.add(importName[0]);
    }
    return override;
  }

  const okResponse = operation.responses?.["200"] ?? operation.responses?.["201"];
  const schema = okResponse?.content?.["application/json"]?.schema;
  if (schema?.$ref) {
    const name = schema.$ref.split("/").at(-1);
    typeImports.add(name);
    return name;
  }
  if (schema?.type === "array" && schema.items?.$ref) {
    const name = schema.items.$ref.split("/").at(-1);
    typeImports.add(name);
    return `${name}[]`;
  }
  return "unknown";
}

function runtimePath(routePath) {
  return routePath.replace(apiBasePath, "") || "/";
}

function operationArgsType(pathParamsType, queryParamsType) {
  const fields = [];
  if (pathParamsType) fields.push(`path: ${pathParamsType}`);
  if (queryParamsType) fields.push(`query?: ${queryParamsType}`);
  return fields.length > 0 ? `{ ${fields.join("; ")} }` : null;
}

function operationArgsUsage(hasPathParams, hasQueryParams) {
  if (!hasPathParams && !hasQueryParams) return "";
  return "params";
}

function operationArgsRequired(pathParamsType) {
  return pathParamsType ? "" : "?";
}

function generatedHeader(spec) {
  return `// This file is generated by scripts/generate-openapi-query-options.mjs.
// Source: ../internal/openapi/openapi.yaml
// OpenAPI: ${spec.info?.title ?? "API"} ${spec.info?.version ?? ""}

`;
}

function renderOperations(operations) {
  const keys = operations
    .map((operation) => {
      const args = operation.argsType
        ? `(params${operation.argsRequired}: ${operation.argsType})`
        : "()";
      const arg = operation.argsType ? ", params" : "";
      return `  ${operation.name}: ${args} => key("${operation.method.toUpperCase()}", "${operation.routePath}"${arg}),`;
    })
    .join("\n");

  const options = operations
    .map((operation) => {
      const args = operation.argsType
        ? `(params${operation.argsRequired}: ${operation.argsType})`
        : "()";
      const keyArgs = operation.argsType ? "params" : "";
      const pathArg = operation.hasPathParams ? "params.path" : "undefined";
      const queryArg = operation.hasQueryParams ? "params?.query" : "undefined";
      return `  ${operation.name}: ${args} =>
    queryOptions({
      queryKey: openApiQueryKeys.${operation.name}(${keyArgs}),
      queryFn: () =>
        requestJson<${operation.responseType}>(
          buildPath("${operation.runtimePath}", ${pathArg}),
          ${queryArg},
        ),
    }),`;
    })
    .join("\n");

  return { keys, options };
}

function renderFile(spec, operations) {
  const { keys, options } = renderOperations(operations);
  const imports = Array.from(typeImports).sort();
  const typeImportLine =
    imports.length > 0 ? `import type { ${imports.join(", ")} } from "@/api";\n` : "";

  return `${generatedHeader(spec)}import { queryOptions } from "@tanstack/react-query";
${typeImportLine}
type PathValue = string | number | boolean;
type QueryValue = PathValue | null | undefined;
type OperationParams = {
  path?: Record<string, PathValue>;
  query?: Record<string, QueryValue>;
};

function key(method: string, routePath: string, params?: OperationParams) {
  return ["openapi", method, routePath, params ?? {}] as const;
}

function buildPath(template: string, pathParams?: Record<string, PathValue>) {
  if (!pathParams) return template;
  return template.replace(/\\{([^}]+)\\}/g, (_, name: string) =>
    encodeURIComponent(String(pathParams[name])),
  );
}

function appendQuery(pathname: string, query?: Record<string, QueryValue>) {
  if (!query) return pathname;
  const searchParams = new URLSearchParams();
  for (const [name, value] of Object.entries(query)) {
    if (value === undefined || value === null) continue;
    searchParams.set(name, String(value));
  }
  const queryString = searchParams.toString();
  return queryString ? \`\${pathname}?\${queryString}\` : pathname;
}

async function requestJson<TResponse>(
  pathname: string,
  query?: Record<string, QueryValue>,
): Promise<TResponse> {
  const response = await fetch(appendQuery(pathname, query), {
    credentials: "include",
  });
  if (response.status === 204) return undefined as TResponse;
  const text = await response.text();
  let data: unknown;
  try {
    data = text ? JSON.parse(text) : undefined;
  } catch {
    data = undefined;
  }
  if (!response.ok) {
    const error = (data as { error?: string } | undefined)?.error;
    throw new Error(error || text.trim() || response.statusText);
  }
  return data as TResponse;
}

export const openApiQueryKeys = {
${keys}
} as const;

export const openApiQueryOptions = {
${options}
} as const;
`;
}

async function main() {
  const source = await readFile(specPath, "utf8");
  const spec = YAML.parse(source);
  const usedNames = new Set();
  const operations = [];

  for (const [routePath, pathItem] of Object.entries(spec.paths ?? {})) {
    for (const method of methodOrder) {
      const operation = pathItem?.[method];
      if (!operation || method !== "get") continue;

      const parameters = collectParameters(pathItem, operation);
      const pathParamsType = paramsType(parameters, "path");
      const queryParamsType = paramsType(parameters, "query");
      const argsType = operationArgsType(pathParamsType, queryParamsType);

      operations.push({
        name: operationName(method, routePath, usedNames),
        method,
        routePath,
        runtimePath: runtimePath(routePath),
        argsType,
        argsRequired: operationArgsRequired(pathParamsType),
        hasPathParams: Boolean(pathParamsType),
        hasQueryParams: Boolean(queryParamsType),
        responseType: responseType(method, routePath, operation),
      });
    }
  }

  await mkdir(path.dirname(outPath), { recursive: true });
  await writeFile(outPath, renderFile(spec, operations));
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
